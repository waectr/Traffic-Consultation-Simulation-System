package person;

public class GLY {
	private String num;
	private String name;
	private String oth;
	
	
	public GLY(String num, String name, String oth) {
		super();
		this.num = num;
		this.name = name;
		this.oth = oth;
	}
	
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getOth() {
		return oth;
	}
	public void setOth(String oth) {
		this.oth = oth;
	}
	
	
}
